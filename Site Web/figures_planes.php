        <h2>Trigonométrie</h2>
        
        <h6>Définitions</h6>
        
        <p>Si ABC est un triangle rectangle en C, on définit :</p>
        
        
        <div class="gauche">
            <ul class="liste_cours">
                <li>Le cosinus de l'angle &ang;<span class="italic">BAC</span>, noté cos(<span class="italic">BAC</span>), le quotient : <br/>cos(<span class="italic">BAC</span>) = <span class="frac"><sup><span class="italic">AC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">côté adjacent à &ang;BAC</span></sup><span>/</span><sub><span class="italic">hypoténuse</span></sub></span> ;</li>
            
                <li>Le sinus de l'angle &ang;<span class="italic">BAC</span>, noté sin(<span class="italic">BAC</span>), le quotient : <br/>sin(<span class="italic">BAC</span>) = <span class="frac"><sup><span class="italic">AC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">côté opposé à &ang;BAC</span></sup><span>/</span><sub><span class="italic">hypoténuse</span></sub></span> ;</li>
                
                <li>La tangente de l'angle &ang;<span class="italic">BAC</span>, noté tan(<span class="italic">BAC</span>), le quotient : <br/>tan(<span class="italic">BAC</span>) = <span class="frac"><sup><span class="italic">AC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">côté opposé à &ang;BAC</span></sup><span>/</span><sub><span class="italic">côté adjacent à &ang;BAC</span></sub></span> ;</li>
                
            </ul>
        
        </div>
        
        <p class="image"><img src="images/figure_img6.png" alt="trigo 1"/></p>
        
        
        <h6>Exemple</h6>
        
        <div class="gauche">
        <p>ABC est un triangle rectangle en B tel que <span class="italic">AB</span> = 5 <span class="italic">cm</span> et <span class="italic">&ang;BAC</span> = 33° .</p>
        <p>Calculer <span class="italic">&ang;BCA</span>, BC et AC (on arrondira les
longueurs au dixième).</p>
        
        
        </div>
        
        <p class="image"><img src="images/figure_img7.png"/></p>
        
        <p>90 &minus; 33 = 57 donc <span class="italic">&ang;BCA</span> = 57° .</p>
        <p>cos(<span class="italic">BAC</span>) = <span class="frac"><sup><span class="italic">AB</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> (car [AB] est le côté adjacent à l'angle <span class="italic">&ang;BAC</span> ).</p>
        
        <p>cos(33) = <span class="frac"><sup>5</sup><span>/</span><sub><span class="italic">AC</span></sub></span></p>
        
        <p><span class="italic">AC</span> &times; cos(33) = 5</p>
        
        <p><span class="italic">AC</span> = <span class="frac"><sup>5</sup><span>/</span>cos(33)</sub></span> &asymp; 6. [AC] mesure environ 6 cm.</p>

        <p>tan(<span class="italic">&ang;BAC</span>) = <span class="frac"><sup><span class="italic">BC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span>  (car [BC] est le côté opposé à l'angle <span class="italic">&ang;BAC</span> et [AB] est le côté adjacent à l'angle
<span class="italic">&ang;BAC</span> ).</p>

        <p>tan(33) = <span class="frac"><sup><span class="italic">BC</span></sup><span>/</span><sub>5</sub></span></p>

        <p><span class="italic">BC</span> = 5 &times; tan(33) &asymp; 3,2. [BC] mesure environ 3,2 cm.</p>
        
    

