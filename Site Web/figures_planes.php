<h6>Propriétés</h6>

<p>Si <span class="italic">x</span> désigne la mesure d'un angle aigu, on a les relations suivantes :</p>

<p>(cos <span class="italic">x</span>)<sup>2</sup> + (sin <span class="italic">x</span>)<sup>2</sup> = 1</p>

<p>tan <span class="italic">x</span> = <span class="frac"><sup>sin <span class="italic">x</span></sup><span>/</span><sub>cos <span class="italic">x</span></sub></span></p>


<p>Preuve :</p>


<p>On se place dans un triangle ABC rectangle en B. Appelons <span class="italic">x</span> la mesure en degrés de l'angle <span class="italic">&ang;BCA</span> :</p>

<p>sin <span class="italic">x</span> = <span class="frac"><sup><span class="italic">AB</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> , cos <span class="italic">x</span> = <span class="frac"><sup><span class="italic">BC</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> , tan <span class="italic">x</span> = <span class="frac"><sup><span class="italic">AB</span></sup><span>/</span><sub><span class="italic">BC</span></sub></span></p>

<div class="gauche">

<p>(sin <span class="italic">x</span>)<sup>2</sup> + (cos <span class="italic">x</span>)<sup>2</sup> =( <span class="frac"><sup><span class="italic">AB</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> )<sup>2</sup> + ( <span class="frac"><sup><span class="italic">BC</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> )<sup>2</sup> = <span class="frac"><sup><span class="italic">AB</span><sup>2</sup> + <span class="italic">BC</span><sup>2</sup></sup><span>/</span><sub><span class="italic">AC</span><sup>2</sup></sub></span> = <span class="frac"><sup><span class="italic">AC</span><sup>2</sup></sup><span>/</span><sub><span class="italic">AC</span><sup>2</sup></sub></span> = 1</p>

<p>car <span class="italic">AB</span><sup>2</sup> + <span class="italic">BC</span><sup>2</sup> = <span class="italic">AC</span><sup>2</sup> d'après
le théorème de Pythagore</p>
    
    
    
