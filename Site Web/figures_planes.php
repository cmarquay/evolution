    <h2>Propriété de Thalès</h2>
        
        
    <h4>La théorème de Thalès</h4>
        
    <h6>Enoncé</h6>
        
    <p>A, B, M sont alignés et A, C, N sont alignés. Si les droites (<span class="italic">BC</span>) et (<span class="italic">MN</span>) sont parallèles, alors, <span class="frac"><sup><span class="italic">AM</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">AN</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> = <span class="frac"><sup><span class="italic">MN</span></sup><span>/</span><sub><span class="italic">BC</span></sub></span> .</p>
        
        
        <p>On se trouve dans l'un des trois cas suivants :</p>
        
        <p><img src="images/figure_img1.png" alt="thales 1"/></p>
        
        <h6>Utilisations</h6>
        
        <p>- Calculer une longueur quand on a deux droites sécantes coupées par deux droites parallèles (voir exemple 1) ;</p>
        
        <p>- Prouver que deux droites ne sont pas parallèles (voir exemple 2).</p>
        
        
       <h6>Exemple 1</h6> 
        
        
        <div class="gauche">
        <p>Enoncé : On considère la figure ci-contre, où A, B et C appartiennent à la droite (d), et D et E appartiennent à la droite (d’). Les droites (BD) et (CE) sont parallèles. On donne <span class="italic">AB</span> = 2 <span class="italic">cm</span>, <span class="italic">AD</span> = 2,8 <span class="italic">cm</span>, <span class="italic">AE</span> = 7 <span class="italic">cm</span> et <span class="italic">CE</span> =10 <span class="italic">cm</span> . Calculer AC et BD.</p>
        
        </div>
        
        
        <p class="image"><img src="images/figure_img2.png" alt="thales 2"/></p>
        
        
        <p>Solution : Les points D, A, E sont alignés et les points B, A, C sont alignés. Les droites (BD) et (CE) sont parallèles. D’après le théorème de Thalès : <span class="frac"><sup><span class="italic">AB</span></sup><span>/</span><sub><span class="italic">AC</span></sub></span> = <span class="frac"><sup><span class="italic">AD</span></sup><span>/</span><sub><span class="italic">AE</span></sub></span> = <span class="frac"><sup><span class="italic">BD</span></sup><span>/</span><sub><span class="italic">CE</span></sub></span>. On a alors <span class="frac"><sup>2</sup><span>/</span><sub><span class="italic">AC</span></sub></span> = <span class="frac"><sup>2,8</sup><span>/</span><sub>7</sub></span> = <span class="frac"><sup><span class="italic">BD</span></sup><span>/</span><sub>10</sub></span> .</p>
        
        
        <p><span class="frac"><sup>2</sup><span>/</span><sub><span class="italic">AC</span></sub></span> = <span class="frac"><sup>2,8</sup><span>/</span><sub>7</sub></span></p>
        
        <p><span class="italic">AC</span> = <span class="frac"><sup>2 &times; 7</sup><span>/</span><sub>2,8</sub></span> = 5 <span class="italic">cm</span></p>
        
        <br/>
        
        <p><span class="frac"><sup>2,8</sup><span>/</span><sub>7</sub></span> = <span class="frac"><sup><span class="italic">BD</span></sup><span>/</span><sub>10</sub></span></p>
        
        <p><span class="italic">BD</span> = <span class="frac"><sup>2,8 &times; 10</sup><span>/</span><sub>7</sub></span> = 4 <span class="italic">cm</span></p>
        
        
        <div class="gauche">
        
        <h6>Exemple 2</h6>
        
        <p>Enoncé : On considère la figure ci-contre.</p>
        <p>On donne <span class="italic">OR</span> = 3 <span class="italic">cm</span>, <span class="italic">OT</span> = 4 <span class="italic">cm</span> , <span class="italic">RS</span> =1,5 <span class="italic">cm</span> et TU =1,8 cm . Les droites (RT) et (SU) sont-elles
parallèles ?</p>
        
        
        </div>
        
        <p class="image"><img src="images/figure_img3.png" alt="thales 3"/></p>
        
        
        
        <p>Solution : <span class="frac"><sup><span class="italic">OR</span></sup><span>/</span><sub><span class="italic">OS</span></sub></span> = <span class="frac"><sup>3</sup><span>/</span><sub>4,5</sub></span> &asymp; 0,67 et <span class="frac"><sup><span class="italic">OT</span></sup><span>/</span><sub><span class="italic">OU</span></sub></span> = <span class="frac"><sup>4</sup><span>/</span><sub>5,8</sub></span> &asymp; 0,69 donc <span class="frac"><sup><span class="italic">OR</span></sup><span>/</span><sub><span class="italic">OS</span></sub></span> &ne; <span class="frac"><sup><span class="italic">OT</span></sup><span>/</span><sub><span class="italic">OU</span></sub></span> . On en déduit que les droites (RT) et
(SU) ne sont pas parallèles (sinon, d’après le théorème de Thalès, les deux quotients seraient égaux).</p>
        
        
        
     <h4>2) Réciproque du théorème de Thalès</h4>   
        
        
        <h6>Enoncé</h6>
        
        <p>A, B, M sont alignés et A, C, N sont alignés. Si <span class="frac"><sup><span class="italic">AM</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">AN</span></sup><span>/</span><sub><span class="italic">AN</span></sub></span> et si les points A, B, M et les points A, C, N sont dans le même ordre, alors les droites (<span class="italic">BC</span>) et (<span class="italic">MN</span>) sont parallèles.</p>
        
        
        <p>On se trouve dans l'un des trois cas suivants :</p>
        
        
        <p><img src="images/figure_img4.png" alt="thales 4"/></p>
        
        
        <h6>Utilisation : </h6>
        
        <p>Prouver que deux droites sont parallèles (voir exemple 3).</p>
        
        <h6>Exemple 3</h6>
        
    
        <div class="gauche">
        <p>Enoncé : (d) et (d') sont deux droites sécantes en A. B et C appartiennent à la droite (d'). D et E appartiennent à la droite (d). On donne : <span class="italic">AC</span> = 3 <span class="italic">cm</span> , <span class="italic">AB</span> = 5 <span class="italic">cm</span> , <span class="italic">AD</span> = 6,5 <span class="italic">cm</span> et <span class="italic">AE</span> = 3,9 <span class="italic">cm</span> . Les droites (BD) et (CE) sont-elles parallèles ?</p>
        
        </div>
        
        
        <p class="image"><img src="images/figure_img5.png" alt="thales 5"/></p>
        
        <p>Solution : <span class="frac"><sup><span class="italic">AC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup>3</sup><span>/</span><sub>5</sub></span> = 0,6 et <span class="frac"><sup><span class="italic">AE</span></sup><span>/</span><sub><span class="italic">AD</span></sub></span> = <span class="frac"><sup>3,9</sup><span>/</span><sub>6,5</sub></span> = 0,6 donc <span class="frac"><sup><span class="italic">AC</span></sup><span>/</span><sub><span class="italic">AB</span></sub></span> = <span class="frac"><sup><span class="italic">AE</span></sup><span>/</span><sub><span class="italic">AD</span></sub></span>. B, A, C sont alignés et D, A, E sont alignés dans le même ordre. D'après la réciproque du théorème de Thalès, on en déduit que les droites (BD) et (CE) sont parallèles.</p>
        
