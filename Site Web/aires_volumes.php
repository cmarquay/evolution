        <h4>2) Aires et volumes</h4>

        <h6>Le cube</h6>
        <p class="rouge">Volume = a<sup>3</sup></p>
        <p class="vert">Aire totale = 6 x a²</p>

        <h6>Le pavé droit</h6>
        <p class="rouge">Volume = a x b x c</p>

        <h6>Le prisme</h6>
        <p class="rouge">Volume = Aire de la base x h</p>
        <p class="vert">Aire latérale = périmètre de la base x h</p>

        <h6>Le cylindre</h6>
        <p class="rouge">Volume = π r² h</p>
        <p class="vert">Aire latérale = 2 π r h</p>

        <h6>La pyramide</h6>
        <p class="rouge">Volume = <span class="frac"><sup><span class="italic">Aire de la base x h</span></sup><span>/</span><sub><span class="italic">3</span></sub></p>

        <h6>Le cône</h6>
        <p class="rouge">Volume = <span class="frac"><sup><span class="italic">π r² h</span></sup><span>/</span><sub><span class="italic">3</span></sub></p>

        <h6>La boule</h6>
        <p class="rouge">Volume = <span class="frac"><sup><span class="italic">4 π r<sup>3</sup></span></sup><span>/</span><sub><span class="italic">3</span></p>
        <p class="vert">Aire de la sphère = 4 π r²</p>


