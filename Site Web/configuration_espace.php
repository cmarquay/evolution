    <h4>Agrandissements et réductions</h4>
    
    <h6>Définition</h6>
    
    <p>Lorsque deux figures ont la même forme, on peut calculer le coefficient suivant :</p>
    
    
    <p><span class="italic">k</span> = <span class="frac"><sup><span class="italic">longueur finale</span></sup><span>/</span><sub><span class="italic">longueur initiale</span></sub></span></p>
    
    <ul id="liste_cours">
        <li>si <span class="italic">k</span> &gt 1, on dit qu'il s'agit un agrandissement ;</li>
        <li>si <span class="italic">k</span> &lt 1, on dit qu'il s'agit d'une réduction ;</li>
    </ul>
    
    <h6>Propriété</h6>
    
    <p>Dans un agrandissement ou une réduction de rapport <span class="italic">k</span> :</p>
    
    <ul id="liste_cours">
        <li>les longueurs sont multipliées par <span class="italic">k</span> ;</li>
        <li>les aires sont multipliées par <span class="italic">k</span><sup>2</sup> ;</li>
        <li>les volumes sont multipliés par <span class="italic">k</span><sup>3</sup> .</li>
    </ul>
    
    
    <div class="gauche">
    
    <h6>Exemple</h6>
    
    <p>Reprenons l'exemple de la pyramide</p>
    <p>On suppose que <span class="italic">SA</span> = 12 <span class="italic">cm</span> et <span class="italic">SA</span>' = 4 <span class="italic">cm</span> .</p>
    <p>Si on s'intéresse au passage de la pyramide SABCD à la pyramide SA'B'C'D', il s'agit d'une réduction car <span class="italic">k</span> = <span class="frac"><sup><span class="italic">SA</span>'</sup><span>/</span><sub><span class="italic">SA</span></sub></span> = <span class="frac"><sup>4</sup><span>/</span><sub>12</sub></span> = <span class="frac"><sup>1</sup><span>/</span><sub>3</sub></span> .</p>
    
        <p>Le volume de la petite pyramide est égal à <span class="frac"><sup>1</sup><span>/</span><sub>27</sub></span> du volume de la grande pyramide car ( <span class="frac"><sup>1</sup><span>/</span><sub>3</sub></span> )<sup>3</sup> = <span class="frac"><sup>1</sup><span>/</span><sub>27</sub></span> .</p>
  
        
