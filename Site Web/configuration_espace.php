    <h2>Angles inscrits - Polygones réguliers</h2>
    
    
    
    
    <h4>Angle inscrit</h4>
    
    
    <div class="gauche">
    <h6>Définition</h6>
    
    <p><span class="cercle">C</span> désigne un cercle de centre <span class="centre">O</span>. A, M et B sont 3 points distints du cercle <span class="cercle">C</span>. On dit que l'angle &ang;<span class="italic">AMB</span> est un angle inscrit dans le cercle <span class="cercle">C</span>, qu'il intercepte l'arc <span class="italic">AB</span> et que l'angle &ang;<span class="italic">AOB</span> est l'angle au centre associé à &ang;<span class="italic">AMB</span>.</p>
    
    </div>
    
    <p class="image"><img src="images/section_img10.png" alt="cercle 1"/></p>
    
    
    <h6>Théorème de l'angle inscrit</h6>
    
    <p>Si M, A et B sont trois points distincts d'un cercle <span class="cercle">C</span> et si M appartient au grand arc de cercle <span class="italic">AB</span>, alors : &ang;<span class="italic">AMB</span> = <span class="frac"><sup>&ang;<span class="italic">AOB</span></sup><span>/</span><sub>2</sub></span> (autrement dit, un angle inscrit dans le cercle <span class="cercle">C</span> mesure la moitié de l'angle au centre qui lui est associé).</p>
    
    
    
    
    <h4>Polygones réguliers</h4>
    
    <h6>Définition</h6>
    <p>Un polygone est une figure à plusieurs côtés. Un polygone régulier est un polygone dont tous les côtés ont la même longueur et tous les angles la même mesure.</p>
    
    <h6>Propriété</h6>
    <p>Il existe un cercle passant par tous les sommets d'un polygone régulier. Ce cercle est appelé cercle circonscrit au polygone régulier. Le centre de ce cercle est appelé centre du polygone.</p>
    
    
    
