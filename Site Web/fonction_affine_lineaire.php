        <h2>Fonctions linéaires et fonctions affines</h2>
        
        
        <h4>Fonctions linéaires</h4>
        
        <h5>a) Qu'est-ce qu'une fonction linéaire ?</h5>
        
        <h6>Définition</h6>
        <p>On appelle fonction liénaire de coefficient <span class="italic">a</span> la fonction définie de la manière suivante : <span class="italic">f</span> : <span class="italic">x</span> &#x27FC <span class="italic">ax</span>.</p>
        
        <h6>Exemple</h6>
        <p>La fonction linéaire de coeficient 3 est la fonction <span class="italic">f</span> : <span class="italic">x</span> &#x27FC <span class="italic">3x</span>. L'image de 4 est 12. 18 a pour antécédent 6.</p>
        
        <h5>b) Représentation graphique d'une fonction linéaire</h5>
        
        <h6>Propriété</h6>
        <p>Dans un repère, la représentation graphique d'une fonction linéaire est une droite passant par l'origine.</p>
        
        <h6>Définition</h6>
        <p>On dit que <span class="italic">y</span> = <span class="italic">ax</span> est une équation de cette droite. Le nombre <span class="italic">a</span> est appelé coefficient directeur de la droite.</p>
        
        <h6>Propriété</h6>
        <p>Appelons (<span class="italic">d</span>) la droite d'équation <span class="italic">y</span> = <span class="italic">ax</span>. Appelons <span class="italic">M</span> un point de coordonnées (<span class="italic">x<sub>M</sub></span>;<span class="italic">y<sub>M</sub></span>)</p>
        
        <p>Si <span class="italic">M</span> &isin; (<span class="italic">d</span>), alors ses coordonnées vérifient l'égalité <span class="italic">y<sub>M</sub></span> = <span class="italic">ax<sub>M</sub></span>, alors <span class="italic">M</span> &isin; (<span class="italic">d</span>).</p>
        
        <h6>Exemple</h6>
        
        
        
        <div class="gauche">
        <p>Représenter graphiquement la fonction linéaire <span class="italic">x</span> &#x27FC 2<span class="italic">x</span>.</p>
        <br/>
        <p>D'après ce qui précède, on sait qu'il s'agit d'une droite passant
par l'origine. Pour tracer cette droite, il faut un deuxième point.</p>
            <br/>
        <p><span class="italic">y</span> = 2<span class="italic">x</span> est l'équation de la droite à tracer. Si <span class="italic">x</span> = 1, alors <span class="italic">y</span>  = 2 donc le point de coordonnées (1 ; 2) appartient à cette droite.</p>  
            
        </div>
        
         
    <p class="image"><img src="images/graphique_fonction_lineaire.png" alt="Graphique fonction linéaire"/></p>
        
        
        <h5>c) Déterminer une fonction linéaire par la donnée d'un nombre et de son image</h5>
        
        <h6>Exemple</h6>
        <p>Quelle est la fonction linéaire telle que 6 ait pour image 7 ?</p>
        <p>Une fonction linéaire est de la forme <span class="italic">x</span> &#x27FC <span class="italic">ax</span>. L'image de 6 est 6<span class="italic">a</span>. On veut donc 6<span class="italic">a</span> = 7. On en déduit que a = <span class="frac"><sup>7</sup><span>/</span><sub>6</sub></span> . La fonction linéaire cherchée est <span class="italic">x</span> &#x27FC <span class="frac"><sup>7</sup><span>/</span><sub>6</sub></span> <span class="italic">x</span>.</p>
        
        
        
        
        <h4>2) Fonctions affines</h4>
        
        <h5>a) Qu'est-ce qu'une fonction affine ?</h5>
        
        
        <h6>Définition</h6>
        <p>On appelle fonction affine une fonction du type <span class="italic">x</span> &#x27FC <span class="italic">ax</span> + <span class="italic">b</span>, où <span class="italic">a</span> et <span class="italic">b</span> sont des nombres.</p>
        
        <h6>Exemple</h6>
        <p><span class="italic">f</span> : <span class="italic">x</span> &#x27FC &minus;2<span class="italic">x</span> + 3</p>
        <p><span class="italic">f</span> est une fonction affine. L'image de 2 est &minus;1 ( &minus;2 &times; 2 + 3 = &minus;1). L'antécédent de 7 est &minus;2 (résoudre l'équation &minus;2<span class="italic">x</span> + 3 = 7).</p>
        
        
        
        <h5>b) Représentation graphique d'une fonction affine</h5>
        
        <h6>Propriété</h6>
        <p>Dans un repère, la représentation graphique d'une fonction affine est une droite.</p>
        
        <h6>Définition</h6>
        <p>On dit que <span class="italic">y</span> = <span class="italic">ax</span> + b est une équation de cette droite. Le nombre <span class="italic">a</span> est appelé coefficient directeur de la
droite et <span class="italic">b</span> est l'ordonnée à l'origine.</p>
        
        <h6>Propriété</h6>
        <p>Appelons (<span class="italic">d</span>) la droite d'équation <span class="italic">y</span> = <span class="italic">ax</span> + b. Appelons <span class="italic">M</span> un point de coordonnées (<span class="italic">x<sub>M</sub></span> ; <span class="italic">y<sub>M</sub></span>)</p>
        
        <p>Si <span class="italic">M</span> &isin; (<span class="italic">d</span>), alors ses coordonnées vérifient l'égalité <span class="italic">y<sub>M</sub></span> = <span class="italic">ax<sub>M</sub></span> + <span class="italic">b</span>.</p>
        
        <p>Réciproquement, si les coordonnées de <span class="italic">M</span> vérifient l'égalité <span class="italic">y<sub>M</sub></span> = <span class="italic">ax<sub>M</sub></span> + <span class="italic">b</span>, alors <span class="italic">M</span> &isin; (<span class="italic">d</span>)</p>
        
        <h6>Exemple</h6>
        
        
        <div class="gauche">
            <p>Représenter graphiquement la fonction affine <span class="italic">x</span> &#x27FC 2<span class="italic">x</span>&minus;3.</p>
            <p>D'après ce qui précède, on sait qu'il s'agit d'une droite.</p>
            <p>Pour tracer cette droite, il faut deux points.</p>
            <p><span class="italic">y</span> = 2<span class="italic">x</span>&minus;3 est l'équation de la droite à tracer.</p>
            <br/>
            <p>Si <span class="italic">x</span> = 0, alors <span class="italic">y</span> = &minus;3 donc le point de coordonnées (0 ; &minus;3) appartient à la droite.</p>
            <br/>
             <p>Si <span class="italic">x</span> = 2 alors <span class="italic">y</span> = 1 donc le point de coordonnées (2 ; 1) appartient à la droite.</p>       
        </div>
        
        
        <p class="image"><img src="images/graphique_fonction_affine.png" alt="Graphique fonction affine"/></p>
        
        <h5>c) Déterminer une fonction affine par la donnée de deux nombres et leurs images</h5>
        
        <h6>Propriété</h6>
        <p>Si <span class="italic">f</span> est une fonction affine, alors les accroissements de <span class="italic">f</span>(<span class="italic">x</span>) et de <span class="italic">x</span> sont proportionnels, le coefficient de proportionnalité étant <span class="italic">a</span>. Autrement dit, si <span class="italic">f</span>(<span class="italic">x</span>) = <span class="italic">ax</span> + <span class="italic">b</span>, alors quels que soient les nombres <span class="italic">x</span><sub>1</sub> et <span class="italic">x</span><sub>2</sub> : <span class="italic">f</span>(<span class="italic">x</span><sub>2</sub>) &minus; <span class="italic">f</span>(<span class="italic">x</span><sub>1</sub>) = <span class="italic">a</span>(<span class="italic">x</span><sub>2</sub> &minus; <span class="italic">x</span><sub>1</sub>).</p>
        
        
        
        <h6>Exemple</h6>
        
        <p>Déterminer la fonction affine telle que :</p>
        
        <ul id="liste_cours">
            <li>2 ait pour image &minus;3 ;</li>
            <li>&minus;3 ait pour image 7.</li>
        </ul>
        
        <p>Une fonction affine est de la forme <span class="italic">x</span> &#x27FC <span class="italic">ax</span> + <span class="italic">b</span></p>
        
        <p>D'après la propriété précédente, on a :</p>
        
        
        <p><span class="italic">f</span>(2) &minus; <span class="italic">f</span>(&minus;3) = <span class="italic">a</span>(2&minus;(&minus;3))</p>
        
        <p>&minus;3&minus;7 = a &times; 5</p>
        
        <p>5<span class="italic">a</span> = &minus;10</p>
        
        <p><span class="italic">a</span> = &minus;<span class="frac"><sup>10</sup><span>/</span><sub>5</sub></span> = &minus;2</p>
        <br/>
        
        <p>On sait maintenant que la fonction affine est de la forme <span class="italic">x</span> &#x27FC &minus;2<span class="italic">x</span> + <span class="italic">b</span></p>
        
        <p>L'image de 2 est &minus;4 + <span class="italic">b</span> = &minus;3 . On en déduit :</p>
        <p><span class="italic">b</span> = &minus;3 + 4 = 1</p>
        
        <p>La fonction affine cherchée est <span class="italic">x</span> &#x27FC &minus;2<span class="italic">x</span> + 1.</p>
    
