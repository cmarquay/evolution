        <h2>Grandeurs composées, changement d’unités</h2>



        <h4>1) Grandeur</h4>
        <p>Une grandeur est constituée d'une valeur mesurée par rapport à une unité de référence.</p>

        <h4>2) Grandeur composée produit</h4>
        <p>C'est une nouvelle grandeur obtenue par multiplication d'autres grandeurs.</p>
        <h6>Exemple:</h6>
        <p>Energie électrique consommée par un appareil = sa puissance . la durée d'utilisation</p>
        <p>E = P . t</p>
        <p>unités kWh = kW . H</p>

        <h4>3) Grandeur composée quotient</h4>
        <p>C'est une nouvelle grandeur obtenue par division d'une grandeur par une autre.</p>
        <h6>Exemple:</h6>
        <p>vitesse moyenne = distance parcourue / durée du pacours</p>
        <p>v = d / t</p>
        <p>unités m / s</p>

        <h4>4) Changements d'unités</h4>
        <p>Pour changer d'unité, il faut remplacer chaque unité par son équivalent dans l'unité souhaitée et effectuer les produits ou quotients indiqués.</p>
        <h6>Exemple:</h6>
        <p>Conversion d'une vitesse donnée en km/h en m/s</p>
        <p>On sait que 1 h = 3600 s et 1 km = 1000 m</p>
        <p>d'où 1 km/h = 1 km / 1 h = 1000 m / 3600 s = 1 m / 3,6 s</p>
        <p>une vitesse de 40 km/h devient v = 40 . 1 3,6 = 11,1 m/s</p>

        <h4>5) Quelques équivalences à retenir</h4>
        <p>temps – durée : 1 an = 365 jours ; 1 jour = 24 h ; 1 h = 60 min ; 1 min = 60 s</p>
        <p>volumes : 1 m3 = 1000 L</p>

