        <h2>Equations et inéquations du premier degré</h2>



        <h4>1) Equations</h4>

            <p>Une équation est une égalité dans laquelle intervient un nombre inconnu, le plus souvent désigné par une lettre.</p>
            <p>Résoudre une équation revient à trouver toutes les valeurs possibles du nombre inconnu vérifiant l'égalité.</p><br/>
            <p>On peut ajouter ou retrancher les deux membres d'une égalité par un même nombre sans changer les solutions de l'équation.</p>
            <p>a = b → a + c = b + c → a – c = b – c</p><br/>
            <p>On peut multiplier ou diviser les deux membres d'une égalité par un même nombre différent de zéro sans changer les solutions de l'équation.</p>
            <p>Si c différent de zéro, a = b → a x c = b x c → <sup>a</sup>∕<sub>c</sub> = <sup>b</sup>∕<sub>c</sub></p>

        <h4>2) Inéquations</h4>

            <p>De la même façon, on peut ajouter ou retrancher les deux membres d'une inégalité par un même nombre sans changer le sens de l'inéquation.</p>
            <p>a > b → a + c > b + c → a – c > b – c</p><br/>
            <p>On peut multiplier ou diviser les deux membres d'une égalité par un même nombre strictement positif sans changer le sens de l'inéquation.</p>
            <p>Si c > 0, a > b → a x c > b x c → <sup>a</sup>∕<sub>c</sub> > <sup>b</sup>∕<sub>c</sub></p><br/>
            <p>Enfin, on peut multiplier ou diviser les deux membres d'une égalité par un même nombre strictement négatif à condition de changer le sens de l'inéquation.</p>
            <p>Si c < 0, a > b → a x c < b x c → <sup>a</sup>∕<sub>c</sub> < <sup>b</sup>∕<sub>c</sub></p>

